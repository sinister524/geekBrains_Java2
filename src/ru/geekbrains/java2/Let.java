package ru.geekbrains.java2;

public class Let {

    private String name;
    private int energyNeed;

    public Let(String name, int energyNeed) {
        this.name = name;
        this.energyNeed = energyNeed;
    }

    public String getName() {
        return name;
    }

    public int getEnergyNeed() {
        return energyNeed;
    }
}
